#!/usr/bin/env node
// Pulls every row from the legacy `deadlockle-votes` D1 and writes a
// SQL file that, when run against the canonical `owdle-votes` D1,
// inserts those rows with source='deadlockle'. The voter_hash salt
// differs by project, so no (game_id, voter_hash) collision with
// existing OWdle rows is possible — but ON CONFLICT DO NOTHING is
// kept as a safety net so re-running the import is idempotent.
//
// Usage:
//   node scripts/migrate-deadlockle-votes.mjs
//     → writes db/migration-import-deadlockle.sql
//   wrangler d1 execute owdle-votes --remote --file=db/migration-import-deadlockle.sql
//     → applies it
//
// Requires wrangler to be authed and to know about both D1s on this
// account. Honours NPM_CONFIG_CACHE for the local-cache workaround.

import { execFileSync } from "node:child_process";
import { writeFileSync, mkdirSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, "..");
const outFile = resolve(repoRoot, "db/migration-import-deadlockle.sql");

const SOURCE_DB = "deadlockle-votes";

function sqlQuote(value) {
  if (value === null || value === undefined) return "NULL";
  if (typeof value === "number") return String(value);
  return `'${String(value).replace(/'/g, "''")}'`;
}

function fetchRows() {
  // wrangler prints a JSON envelope to stdout when --json is passed; the
  // exact shape is `[{ results: [...], success: true, meta: {...} }]`.
  const out = execFileSync(
    "npx",
    [
      "wrangler",
      "d1",
      "execute",
      SOURCE_DB,
      "--remote",
      "--json",
      "--command",
      "SELECT game_id, voter_hash, game_name, game_image, game_released, created_at FROM votes",
    ],
    { encoding: "utf8", stdio: ["ignore", "pipe", "inherit"] },
  );
  const parsed = JSON.parse(out);
  const first = Array.isArray(parsed) ? parsed[0] : parsed;
  return first?.results ?? [];
}

function buildSql(rows) {
  // D1 rejects BEGIN TRANSACTION / COMMIT in raw SQL files because the
  // underlying Durable Object handles transactionality at a different
  // layer; each statement in a --file run is its own atomic unit.
  const header = [
    "-- Generated by scripts/migrate-deadlockle-votes.mjs",
    `-- Source: ${SOURCE_DB}`,
    `-- Rows:   ${rows.length}`,
    "-- Apply with:",
    "--   wrangler d1 execute owdle-votes --remote --file=db/migration-import-deadlockle.sql",
    "",
  ];
  const inserts = rows.map((r) => {
    const vals = [
      sqlQuote(r.game_id),
      sqlQuote(r.voter_hash),
      sqlQuote(r.game_name),
      sqlQuote(r.game_image),
      sqlQuote(r.game_released),
      sqlQuote(r.created_at),
      "'deadlockle'",
    ].join(", ");
    return (
      `INSERT INTO votes (game_id, voter_hash, game_name, game_image, game_released, created_at, source) ` +
      `VALUES (${vals}) ON CONFLICT(game_id, voter_hash) DO NOTHING;`
    );
  });
  return [...header, ...inserts, ""].join("\n");
}

function main() {
  const rows = fetchRows();
  if (rows.length === 0) {
    console.error(`No rows returned from ${SOURCE_DB}. Aborting.`);
    process.exit(1);
  }
  const sql = buildSql(rows);
  mkdirSync(dirname(outFile), { recursive: true });
  writeFileSync(outFile, sql, "utf8");
  console.log(`Wrote ${rows.length} INSERTs → ${outFile}`);
  console.log(
    "Apply with:\n  npx wrangler d1 execute owdle-votes --remote --file=db/migration-import-deadlockle.sql",
  );
}

main();
